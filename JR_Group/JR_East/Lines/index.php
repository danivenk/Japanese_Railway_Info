			<div class="img"><a href="../../../IMG/JR_Yamanote_Line.jpg" target="_blank"><img src="../../../IMG/JR_Yamanote_Line-Small.jpg" width="100%"></a><br>
			JR Yamanote Line E231 serie 500 for Tokyo and Shinagawa. (the clockwise line)</div>
			<p>The Yamanote Line (Japanese: 山手線) is a ring line around Tokyo. It connects the major stations of Tokyo and it's urban centers including Shinjuku Station Marunouchi Station, Ikebukuro Station, Tokyo Station, Shibuya Station, Yurakucho and Ginza areas. The whole loop has 29 stations where all trains stop. It takes about an hour to travel around the loop, trains travel with an interval of 2,5 minutes in peak hours and 4 minutes at other times.</p>

			<p><strong>Ridership</strong><br>
			An estimated 3.68 million passengers ride every day on Tokyo's Yamanote Line, with its 29 stations. For comparison, the New York City Subway carries 5.08 million passengers per day on 26 lines serving 468 stations, and the London Underground carries 3.36 million passengers per day on 12 lines serving 275 stations.</p>

			<p><strong>History</strong><br>
			The predecessor of the present-day Yamanote Line was opened on 1 March 1885 by the Nippon Railway Company, operating between Shinagawa Station in the south and Akabane Station in the north. The top part of the loop between Ikebukuro and Tabata (a distance of 3.3 km) opened on 1 April 1903, and both lines were merged to become the Yamanote Line on 12 October 1909.</p>

			<p>The line was electrified in 1909, the same year the Osaki - Shinagawa section was double-tracked, with the loop completed in 1925 with the opening of the double track, electrified section between Kanda and Ueno on 1 November, providing a north-south link via Tokyo Station through the city's business centre. A parallel freight line, also completed in 1925, ran along the inner side of the loop between Shinagawa and Tabata.</p>

			<p>During the prewar era, the Ministry of Railways did not issue permits to private suburban railway companies for new lines to cross the Yamanote Line from their terminal stations to the central districts of Tokyo, forcing the companies to terminate services at stations on the line. This policy led to the development of new urban centers (新都心、副都心 shintoshin, fukutoshin) around major transfer points on the Yamanote Line, most notably at Shinjuku and Ikebukuro (which are now the two busiest passenger railway stations in the world).</p>

			<p>The contemporary Yamanote Line came into being on 19 November 1956 when it was separated from the Keihin-Tōhoku Line and given its own set of tracks along the eastern side of the loop between Shinagawa and Tabata. However, Yamanote Line trains continued to periodically use the Keihin-Tōhoku tracks, particularly on holidays and during off-peak hours, until rapid service trains were introduced on the Keihin-Tōhoku Line in 1988.</p>

			<p>A major explosion on the Yamanote Freight Line in Shinjuku in 1967 led to the diversion of freight traffic to the more distant Musashino Line. To address severe undercapacity, the freight line was repurposed for use by Saikyo Line and Shōnan-Shinjuku Line trains, as well as certain limited express trains such as the Narita Express and some liner services. Likewise, there are currently plans to connect the Tohoku Main Line and Joban Line to the Tokaido Main Line via a new Ueno-Tokyo Line link to provide further relief on the busiest portion of the Yamanote Line today, the southbound segment between Ueno and Okachimachi.</p>

			<p>Automatic train control (ATC) was introduced from 6 December 1981, and digital ATC (D-ATC) was introduced from 30 July 2006.</p>

