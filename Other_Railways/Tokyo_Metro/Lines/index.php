			<h2 class="metro" id="tozai"><a href="../../../IMG/T.png" target="_blank"><img src="../../../IMG/T-Small.png"></a>&nbsp;Tozai Line&nbsp;<span style="background-color: #00A7DB;" class="color">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></h2>
			<div class="img"><a href="../../../IMG/Tokyo-Metro_Tozai_Line.jpg" target="_blank"><img src="../../../IMG/Tokyo-Metro_Tozai_Line-Small.jpg" width="100%"></a><br>
			A Tokyo Metro 05 series EMU between Nishi-Kasai and Minami-Sunamachi</div>
			<p>The Tōzai Line (東西線 Tōzai-sen) is a rapid transit line in Tokyo and Chiba Prefecture, Japan, owned and operated by Tokyo Metro. Its name literally means East-West Line. The line runs between Nakano Station in Nakano, Tokyo and Nishi-Funabashi Station in Funabashi, Chiba. The Tōzai Line was referred to as Line 5 during the planning stages, thus the seldom-used official name is Line 5 Tōzai Line (5号線東西線 Go-gō-sen Tōzai-sen). This line rides on a overhead power supply.</p>

			<p><strong>Overview</strong><br>
			Trains run through onto the East Japan Railway Company (JR East)'s Chūō-Sōbu Line for Mitaka and the Tōyō Rapid Railway Line for Tōyō-Katsutadai, making the line an alternative route to the Chūō-Sōbu between Nakano and Nishi-Funabashi.</p>

			<p>According to the Tokyo Metropolitan Bureau of Transportation, as of June 2009 the Tokyo Metro Tōzai Line was the most crowded subway line in Tokyo, at its peak running at 199% capacity between Kiba and Monzen-Nakachō stations.[2] Women-only cars were introduced on the line during morning rush hours starting on November 20, 2006.</p>

			<p>On maps, diagrams and signboards, the Tōzai Line is shown using the color "sky blue", and its stations are given numbers using the letter T.</p>

			<p><strong>Service</strong><br>
			The Tōzai Line was the first Tokyo Metro line on which express services run: three types of rapid trains skip some stations east of Tōyōchō. The Fukutoshin Line began services on June 14, 2008 and also features express services.</p>

			<p>Through services to Mitaka via the JR East Chūō-Sōbu Line and Tōyō-Katsutadai via the Tōyō Rapid Railway run all day. During the morning and evening peak periods, through services run to Tsudanuma via the JR East Sōbu Main Line.</p>

			<p><strong>History</strong><br>
			The Tōzai Line was planned by a review committee of the then Ministry of Transportation in 1962 and numbered Line 5. Its name literally means "East-West Line", and it was primarily planned to relieve traffic on the busy Sōbu Main Line as well as provide a straight crosstown connection through north-central Tokyo. Although this corridor is now served by the Tokyo Metropolitan Bureau of Transportation (Toei) Shinjuku Line and JR Keiyō Line as well, the Tōzai Line continues to operate beyond capacity due to its accessibility to other lines, as well as to growing condominium developments in eastern Tokyo.</p>

			<p>The Takadanobaba to Kudanshita section opened in 1964, and the remainder opened in stages until its completion in 1969. Through service with the then Japan National Railways began in 1969 connecting the Chūō and Sōbu lines.</p>

			<p>The Tōyō Rapid Railway Line, effectively an eastward extension of the line, opened in 1996.</p>

			<p>Chronology
