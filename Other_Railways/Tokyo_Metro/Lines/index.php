				<li>September 14, 1967: Ōtemachi to Tōyōchō section opens.</li>
				<li>March 29, 1969: Tōyōchō to Nishi-Funabashi section opens and Rapid service begins (non-stop between Tōyōchō and Nishi-Funabashi).</li>
				<li>April 8, 1969: Through service on the Chūō Line is extended to Mitaka, and through service begins on the Sōbu line to Tsudanuma.</li>
				<li>April 8, 1972: Through service on the Sōbu Line is withdrawn except during rush hours.</li>
				<li>1975: Another type of Rapid service is introduced, calling at Urayasu between Tōyōchō and Nishi-Funabashi.</li>
				<li>October 1, 1979: Nishi-Kasai station opens.</li>
				<li>March 27, 1981: Minami-Gyōtoku station opens.</li>
				<li>1986: Commuter Rapid service is introduced, running non-stop between Urayasu and Nishi-Funabashi.</li>
				<li>(April 1, 1987: JNR is privatised. The Chūō and Sōbu lines become the property of JR East.)</li>
				<li>(March 20, 1995: The Tokyo subway Sarin gas attack occurred on the Chiyoda, Marunouchi, and Hibiya lines.)</li>
				<li>1996: The Rapid service that runs non-stop between Tōyōchō and Nishi-Funabashi ceases.</li>
				<li>April 27, 1996: Tōyō Rapid Line opens between Nishi-Funabashi and Tōyō-Katsutadai. Through service begins.</li>
				<li>January 22, 2000: Myōden station opens.</li>
				<li>April 1, 2004: Teito Rapid Transit Authority (TRTA or Eidan) becomes Tokyo Metro.</li>
				<li>November 20, 2006: Women-only cars are introduced during morning rush hours.</li>
			</ul></p>
			
