			<h2 class="metro" id="ginza"><a href="../../../IMG/G.png" target="_blank"><img src="../../../IMG/G-Small.png"></a>&nbsp;Ginza Line&nbsp;<span style="background-color: #FFA500;" class="color">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></h2>
			<div class="img"><a href="../../../IMG/Tokyo-Metro_Ginza_Line.jpg" target="_blank"><img src="../../../IMG/Tokyo-Metro_Ginza_Line-Small.jpg" width="100%"></a><br>
			Tokyo Metro Ginza Line 01 series EMUs at Shibuya Station</div>
			<p>The Tokyo Metro Ginza Line (東京地下鉄銀座線 Tōkyō Chikatetsu Ginza-sen) is a subway line in Tokyo, Japan. It is part of the of Tokyo Metro network. The official name is Line 3 Ginza Line (3号線銀座線 3-gōsen Ginza-sen). It is 14.3 km long and serves the wards of Shibuya, Minato, Chūō, Chiyoda, and Taitō. The line holds the distinction of being the oldest subway line in Asia. On maps, diagrams and signboards, the line is shown using the color orange, and its stations are given numbers using the letter G. The subway has a third rail power source (With that it's 1 of the 2 subway lines that have a thrid rail power source, all other subway lines have overhead power). During the weekdays the Ginza Line operates every 2 minutes in peak hours, every 2 minutes and 15 seconds in evening peak hours and every 3 minutes in the dayhours.</p>

			<p><strong>History</strong><br>
			The Ginza Line was conceived by a businessman named Noritsugu Hayakawa, who visited London in 1914, saw the London Underground and concluded that Tokyo needed its own underground railway. He founded the Tokyo Underground Railway (東京地下鉄道 Tōkyō Chika Tetsudō) in 1920, and began construction in 1925.</p>

			<p>On January 1, 1930, the subway was extended by 1.7 km to temporary Manseibashi Station, abandoned on November 21, 1931 when the subway reached Kanda, 500 meters further down south the line. The Great Depression slowed down construction, but the line finally reached its originally planned terminus of Shinbashi on June 21, 1934.</p>

			<p>In 1938, the Tōkyō Rapid Railway (東京高速鉄道 Tōkyō Kōsoku Tetsudō), a company tied to the predecessor of today's Tokyu Corporation, began service between Shibuya and Toranomon, later extended to Shinbashi in 1939. The two lines began through-service interoperation in 1939 and were formally merged as the Teito Rapid Transit Authority ("Eidan Subway" or "TRTA") in July 1941.</p>

			<p>The "Ginza Line" name was applied in 1953 to distinguish the line from the new Marunouchi Line. In the postwar economic boom, the Ginza Line became increasingly crowded. The new Hanzōmon Line began to relieve the Ginza Line's traffic in the 1980s, but the Ginza Line is still quite crowded as it serves major residential, commercial, and business districts in central Tokyo. According to a June 2009 Tokyo Metropolitan Bureau of Transportation survey, the Ginza Line is the seventh most crowded subway line in Tokyo, running at 168% capacity between Akasaka-Mitsuke and Tameike-Sannō stations.</p>

			<p>The newest station on the line, Tameike-Sannō Station, opened in 1997 to provide a connection to the newly built Namboku Line.</p>

