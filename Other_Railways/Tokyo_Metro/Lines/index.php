			<p>The Tokyo Metro Chiyoda Line (東京地下鉄千代田線 Tōkyō Chikatetsu Chiyoda-sen?) is a rapid transit line owned and operated by Tokyo Metro in Tokyo, Japan.</p>

			<p><strong>Overview</strong><br>
			The 21.9 km main line serves the wards of Adachi, Arakawa, Bunkyō, Chiyoda, Minato and Shibuya, and a short stretch of tunnel in Taitō with no station. A 2.1 km branch line between Ayase and Kita-Ayase is located in Adachi. Its official name, rarely used, is Line 9 Chiyoda Line (9号線千代田線 kyūgō sen Chiyoda-sen?).</p>

			<p>On maps, diagrams and signboards, the line is shown using the color green, and its stations are given numbers using the letter "C".</p>

			<p>Trains have through running onto other railway lines on both ends. More than half of these are trains to the northeast beyond Ayase onto the East Japan Railway Company (JR East) Joban Line to Toride. The rest run to the southwest beyond Yoyogi-Uehara onto the Odakyū Odawara Line to Hon-Atsugi and to Karakida on the Odakyu Tama Line.</p>

			<p>According to the Tokyo Metropolitan Bureau of Transportation, as of June 2009 the Chiyoda Line was the second most crowded subway line in Tokyo, at its peak running at 181% capacity between Machiya and Nishi-Nippori stations.</p>

			<p><strong>History</strong><br>
			The Chiyoda Line was originally proposed in 1962 as a line from Setagaya in Tokyo to Matsudo, Chiba; the initial name was "Line 8". In 1964, the plan was changed slightly so that through service would be offered on the Joban Line north of Tokyo, and the number was changed to "Line 9".</p>

			<p>Line 9 was designed to pass through built-up areas in Chiyoda, and also intended to relieve the busy Ginza Line and Hibiya Line, which follow a roughly similar route through central Tokyo.</p>

			<p>The first stretch was opened on December 20, 1969 between Kita-Senju and Ōtemachi. The line was almost completed by October 10, 1972 when it reached Yoyogi-Kōen, although the 1 km section to Yoyogi-Uehara was not completed until March 31, 1978. The branch line to Kita-Ayase was opened on December 20, 1979.</p>

			<p>On May 15, 2006, women-only cars were introduced on early-morning trains from Toride on the Joban Line to Yoyogi-Uehara.</p>

			<p>From March 18, 2008, Odakyu Romancecar limited express services began running between Kita-Senju and Hakone-Yumoto (on the Hakone Tozan Line) and Karakida (on the Odakyu Tama Line). Trains also run from/to Shin-Kiba using tracks connecting to the Yurakucho Line. It is the first time that reserved-seating trains have operated on a Japanese subway line.</p>

