			<h2 class="metro" id="hibiya"><a href="../../../IMG/H.png" target="_blank"><img src="../../../IMG/H-Small.png"></a>&nbsp;Hibiya Line&nbsp;<span style="background-color: #C0C0C0;" class="color">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></h2>
			<div class="img"><a href="../../../IMG/Tokyo-Metro_Hibiya_Line-.jpg" target="_blank"><img src="../../../IMG/Tokyo-Metro_Hibiya_Line-Small.jpg" width="100%"></a><br>
			A Tokyo Metro 03 series EMU at Hiroo Station bound for Kita-Senju</div>
			<p>The Tokyo Metro Hibiya Line (東京地下鉄日比谷線 Tōkyō Chikatetsu Hibiya-sen) is a subway line in Tokyo, Japan, owned and operated by Tokyo Metro. The line was named after the district of Hibiya, under which it passes. The official name is Line 2 Hibiya Line (2号線日比谷線 Ni-gōsen Ginza-sen).</p>

			<p><strong>Overview</strong><br>
			The Hibiya Line runs between Naka-Meguro in Meguro and Kita-Senju in Adachi. The line's path is somewhat similar to that of the Ginza Line; however, the Hibiya Line was designed to serve a number of important districts, such as Ebisu, Roppongi, Tsukiji, Kayabachō and Senju, which were not on an existing line.</p>

			<p>The Hibiya Line is connected to the Tobu Skytree Line at Kita-Senju, and through services operate between Naka-Meguro and Tōbu-Dōbutsu-Kōen on the Tobu Skytee Line, and onward to Minami-Kurihashi on the Tōbu Nikkō Line. Some peak-hour services terminate at Takenotsuka, Kita-Koshigaya or Kita-Kasukabe on the Tobu Skytree Line.</p>

			<p>Prior to 16 March 2013, when through-running began between the Tokyo Metro Fukutoshin Line and the Tokyu Toyoko Line, Hibiya Line trains also inter-ran via the Tokyu Toyoko Line to Kikuna.</p>

			<p>According to the Tokyo Metropolitan Bureau of Transportation, as of June 2009 the Hibiya Line is the eighth most crowded subway line in Tokyo, running at 164% capacity between Minowa and Iriya stations.</p>

			<p>On maps, diagrams and signboards, the line is shown using the color "silver", and its stations are numbered with the prefix "H"</p>

			<p><strong>History</strong><br>
			The Hibiya Line was the fourth subway line built in Tokyo after the Ginza Line, Marunouchi Line, and Toei Asakusa Line.

			Its basic plan was drawn up by a Ministry of Transportation committee in 1957. Called "Line 2" at the time, it was designed to connect Naka-Meguro in southwest Tokyo with Kita-Koshigaya in the northeast. The full northeastern extension of the line was never built, as the Tobu Railway upgraded to quadruple track within the same corridor to meet capacity demands.

			Work began in 1959, with the first section opening in March 1961. The line opened in stages: the northern section, between Kita-Senju and Ningyōchō, was operational in May 1962; the southern section, between Naka-Meguro and Kasumigaseki, opened in March 1964. The final segment, bridging Higashi-Ginza and Kasumigaseki, opened on August 29, 1964, just weeks before the opening ceremony for the 1964 Summer Olympics. This was something of a coup for the Teito Rapid Transit Authority (the predecessor of today's Tokyo Metro), as the Toei Asakusa Line, which was also to be completed in time for the Olympics, had fallen behind schedule and remained under construction for the duration of the Games.

			The Hibiya Line was one of the lines targeted in the 1995 Aum sarin gas attack.

			On March 8, 2000, five people were killed and 63 were injured when a derailed Hibiya Line train was sideswiped by a second train near Naka-Meguro Station.</p>

