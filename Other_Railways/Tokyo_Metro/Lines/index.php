			<h2 class="metro" id="marunouchi"><a href="../../../IMG/M.png" target="_blank"><img src="../../../IMG/M-Small.png"></a>&nbsp;<a href="../../../IMG/m_b.png" target="_blank"><img src="../../../IMG/m_b-Small.png"></a>&nbsp;Marunouchi Line&nbsp;<span style="background-color: #FF0000;" class="color">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></h2>
			<div class="img"><a href="../../../IMG/Tokyo-Metro_Marunouchi_Line.jpg" target="_blank"><img src="../../../IMG/Tokyo-Metro_Marunouchi_Line-Small.jpg" width="100%"></a><br>
			A Tokyo Metro 02 series EMU</div>
			<p>The Tokyo Metro Marunouchi Line (東京地下鉄丸ノ内線 Tōkyō Chikatetsu Marunouchi-sen) is a subway line of the Tokyo Metro system in Tokyo, Japan. The line runs in a U-shape between Ogikubo Station in Suginami and Ikebukuro Station in Toshima, with a branch line between Nakano-Sakaue Station and Hōnanchō Station. The official name is Line 4 Marunouchi Line (4号線丸ノ内線 Yon-gōsen Marunouchi-sen). The Marunouchi Line is one of the 2 third rail lines of the Tokyo Metro Subway company. All other lines have overhead power.</p>

			<p><strong>Overview</strong><br>
			The Marunouchi Line is the second line to be built in the city, and the first one constructed after the Second World War. The route is U-shaped, running from Ogikubo Station in the west of the city via the commercial and administrative district of Shinjuku through to the Marunouchi commercial center around Tokyo Station, before turning back and heading to Ikebukuro.</p>

			<p>The Marunouchi Line is served by Tokyo Metro 02 series rolling stock in six-car trains on the main line, and three-car trains on the Hōnanchō branch. The main line is the most frequent subway line in Tokyo, with trains running at intervals of 1 minute 50 seconds during peak hours. In spite of such high-frequency service, according to a 2008 survey by the Ministry of Land, Infrastructure, Transport and Tourism the Marunouchi Line is one of the most crowded railway lines in Tokyo, running at 157% capacity between Shin-ōtsuka and Myōgadani stations. Its age and relatively short train length has made it one of the most crowded lines in Tokyo, although the 2000 opening of the Toei Ōedo Line has relieved the problem somewhat. In response to crowding, Tokyo Metro upgraded all stations with chest-high platform doors on March 28, 2009, a date on which it also began driver-only operation . The Hōnanchō branch switched to driver-only operation in July 2004.</p>

			<p>Due to the age of the Marunouchi Line and the relative shallowness at which it runs, at several points in central Tokyo trains run at or above ground level. These include Yotsuya Station, the Kanda River near Ochanomizu Station (see image), and between Kōrakuen and Myōgadani stations.</p>

			<p>On maps, diagrams and signboards, the line is shown using the color "red". Its stations are given numbers using the letter M; Hōnanchō branch line stations carry a lowercase m.</p>

			<p><strong>History</strong><br>
			The Marunouchi Line is the second subway line to be built in the city, and the first to be constructed after the Second World War. Its design is similar to that of the Ginza Line, the oldest subway line in Tokyo. Both lines are standard gauge and use third-rail power, unlike the other Tokyo subway lines.</p>

			<p>The first section was opened between Ikebukuro and Ochanomizu on 20 January 1954. The subsequent progress of the line was as follows:
